solution :: (Maybe String, Maybe String)
solution =
  let paths = map parsePath input
   in ( Just . show $
          let grouped = group $ sort $ map (foldl move (0, 0)) paths
           in length $ filter (odd . length) grouped,
        Nothing
      )

move :: Coord -> Direction -> Coord
move (x, y) direction
  | even y = coordSum (x, y) $ evenRow direction
  | odd y = coordSum (x, y) $ oddRow direction

coordSum :: (Num a, Num b) => (a, b) -> (a, b) -> (a, b)
coordSum (x1, y1) (x2, y2) = (x1 + x2, y1 + y2)

evenRow :: Direction -> Coord
evenRow NE = (0, -1)
evenRow E = (1, 0)
evenRow SE = (0, 1)
evenRow SW = (-1, 1)
evenRow W = (-1, 0)
evenRow NW = (-1, -1)

oddRow :: Direction -> Coord
oddRow NE = (1, -1)
oddRow E = (1, 0)
oddRow SE = (1, 1)
oddRow SW = (0, 1)
oddRow W = (-1, 0)
oddRow NW = (0, -1)

parsePath :: String -> Path
parsePath [] = []
parsePath (c : s)
  | c == 'e' = E : parsePath s
  | c == 'w' = W : parsePath s
  | otherwise =
    let (cc : ss) = s
     in case [c, cc] of
          "nw" -> NW : parsePath ss
          "ne" -> NE : parsePath ss
          "sw" -> SW : parsePath ss
          "se" -> SE : parsePath ss

type Coord = (Int, Int)

type Path = [Direction]

data Direction = E | SE | SW | W | NW | NE deriving (Show)

